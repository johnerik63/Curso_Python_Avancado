"""
Tipos int e float
int -> Número Inteiro
O tipo int representa qualquer número positivo ou negativo.
Int sem sinal é considerado positivo.

"""
print(11) #int
print(-11) #int
print(0) #int

"""
float -> Número com ponto flutuante. O tipo float
representa qualquer número positivo ou negativo com ponto flutuante.
Float sem sinal é considerado positivo

"""
print(1.1, 10.11) #float
print(0.0, -10.11) #float
#se tem casa decimal é float 

#A função type mostra o tipo que o python inferiu ao valor.
print(type("Pedro"))#Mostra a classe do que está no argumento
print(type(1))
print(type(1.1))
print(type("1.1"))
